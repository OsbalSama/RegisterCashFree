    public void reportException() {
        setClipboard(this.fullReport);
        String Mensaje = "Mensaje Copiado en Portapapeles," + "<br>";
        Mensaje += "Resolveremos el Incidente lo mas pronto posible..." + "<br>" + "<br>";
        Mensaje += "Gracias por tu Ayuda! ;)";
        new Messages().aceptMessage("Reportar Excepcion", Mensaje);
        getHelp();
        this.dispose();
    }

    public void getHelp() {
        systemController.getInstance().getContactPage();
    }

    public void onClipboard() {
        setClipboard(fullReport);
        new Messages().closeMessage("Copiar Mensaje", "Mensaje en Portapapeles");
    }

    public void btnCancel() {
        this.dispose();
    }

    public void setClipboard(String txt) {
        StringSelection text = new StringSelection(txt);
        Toolkit.getDefaultToolkit().getSystemClipboard().setContents(text, this);
    }

