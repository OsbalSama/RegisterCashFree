    public void setData(String Class, String Method, Exception Exception) {
        lblTitulo.setText("Exception Recived");
        this.Exception = Exception.toString();
        this.Class = "Class: " + Class + ".java";
        this.Method = "Function: " + Method;
        this.Reported = new Date();

        fastReport = "***EXCEPTION REPORT***\n";
        fastReport += "-->" + Exception + "\n\n";
        fastReport += "***DATE OF ISSUE***" + "\n";
        fastReport += "-->" + this.Reported;

        fullReport = "***EXCEPTION REPORT***\n";
        fullReport += "-->" + Exception + "\n\n";
        fullReport += "***EXCEPTION CAUSED IN***" + "\n";
        fullReport += "-->" + this.Class + "\n";
        fullReport += "-->" + this.Method + "\n" + "\n";
        fullReport += "***CLIENT INFORMATION***" + "\n";
        fullReport += "-->Client: " + "STIL BE CONFIGURED" + "\n";
        fullReport += "-->Email: " + "STIL BE CONFIGURED" + "\n";
        fullReport += "-->Phone: " + "STIL BE CONFIGURED" + "\n" + "\n";
        fullReport += "***DATE OF ISSUE***" + "\n";
        fullReport += "-->" + this.Reported;

        if (session.getLogedUser() != null && session.getLogedUser().isRoot()) {
            btnReport.setVisible(false);
        } else {
            btnToClipboard.setVisible(false);
        }

        if (session.getLogedUser() != null && session.getLogedUser().isRoot()) {
            txtError.setText(fullReport);
        } else {
            txtError.setText(fastReport);
        }
        txtError.setCaretPosition(0);
    }

