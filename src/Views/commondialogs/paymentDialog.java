    private void txtCashKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCashKeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_ESCAPE) {
            CancelarCompra();
        }
    }//GEN-LAST:event_txtCashKeyPressed

    private void txtCashKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCashKeyReleased
        if (!txtCash.getText().replace(" ", "").equals("")) {
            if (txtCash.getText().equals(".")) {
                txtCash.setText("0.");
            }
        }
        txtCash.setBackground(Color.white);
        recalcPayment();
    }//GEN-LAST:event_txtCashKeyReleased

    public void recalcPayment() {
        try {
            if (txtCash.getText().trim().length() > 0) {
                Cash = Double.parseDouble(txtCash.getText());
                Change = Cash - Charge;

            } else {
                Cash = 0;
                Change = Cash - Charge;
            }
            if (Change > 0) {
                txtChange.setText(dataController.getInstance().getMoneyFormat(Change));
            } else {
                txtChange.setText(dataController.getInstance().getMoneyFormat(0));
            }
        } catch (Exception e) {
            new Messages().errorMessage(Class, "recalcPayment()", e);
        }

    }

    private void txtCashKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCashKeyTyped
        if (!Character.isDigit(evt.getKeyChar()) && evt.getKeyChar() != '.') {
            evt.consume();
        }
        if (evt.getKeyChar() == '.' && txtCash.getText().contains(".")) {
            evt.consume();
        }
    }//GEN-LAST:event_txtCashKeyTyped

