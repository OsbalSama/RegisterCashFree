    public boolean checkFields() {
        boolean resp = false;
        if (Cash > 0 && Change >= 0) {
            resp = true;
        } else {
            txtCash.requestFocus();
            txtCash.setBackground(Color.yellow);
        }
        return resp;
    }

    public void RealizarCompra() {
        if (checkFields()) {
            if (new Messages().confirmMessage("Realizar Compra", "¿Está seguro que desea realizar la Compra?")) {
                setNext(true);
                setSelectedPrinter(cmbPrinters.getSelectedItem().toString());
                this.dispose();
            }
        }
    }
