    public void setData(Sale sale) {
        this.sale = sale;
        lblIdReg.setText("Venta No." + this.sale.getId());
        lblSeller.setText("Vendido por: " + this.sale.getSeller());
        lblSaleDate.setText("Fecha de Venta: " + this.sale.getDateCreatedToString());
        lblSaleTime.setText("Hora de Venta: " + this.sale.getTimeCreatedToString());
        txtTotal.setText(dataController.getInstance().getMoneyFormat(this.sale.getTotalOfSale()));
        btnClose.requestFocus();

        loadTable();
        showDatabase();
    }

    public void loadTable() {
        try {
            modelo = new DefaultTableModel() {
                @Override
                public boolean isCellEditable(int filas, int columnas) {
                    if (columnas < 0) {
                        return true;
                    } else {
                        return false;
                    }
                }
            };
            modelo.addColumn("DescripciÃ³n");
            modelo.addColumn("Marca");
            modelo.addColumn("Precio");
            modelo.addColumn("Cantidad");
            modelo.addColumn("Total");
            tblData.setModel(modelo);
            tblData.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        } catch (Exception e) {
            new Messages().errorMessage(Class, "loadTable()", e);
        }
    }

    public void showDatabase() {
        modelo.setRowCount(0);
        try {
            List<SoldProduct> Solds = DataBase.findSoldProductsbySaleId(sale.getId());
            if (!Solds.isEmpty()) {
                for (int i = 0; i < Solds.size(); i++) {
                    SoldProduct temp = Solds.get(i);
                    String[] newRow = new String[5];
                    newRow[0] = temp.getDescription();
                    newRow[1] = temp.getBrand();
                    newRow[2] = dataController.getInstance().getMoneyFormat(temp.getValue());
                    if (temp.isBulk()) {
                        newRow[3] = dataController.getInstance().getKgFormat(temp.getCant());
                    } else {
                        newRow[3] = dataController.getInstance().getPzFormat(temp.getCant());
                    }
                    newRow[4] = dataController.getInstance().getMoneyFormat(temp.getTotal());
                    modelo.addRow(newRow);
                }
            }
        } catch (Exception e) {
            new Messages().errorMessage(Class, "showDatabase()", e);
        }
    }

