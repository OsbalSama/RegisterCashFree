    public void setData(String[] columnas_excel) {

        cmbBCode.removeAllItems();
        cmbDesc.removeAllItems();
        cmbBrand.removeAllItems();
        cmbType.removeAllItems();
        cmbCounter.removeAllItems();
        cmbInventory.removeAllItems();
        cmbPrice.removeAllItems();

        cmbBCode.addItem("--NO SELECT--");
        cmbBCode.addItem("AUTOGENERAR");

        cmbDesc.addItem("--NO SELECT--");

        cmbBrand.addItem("--NO SELECT--");
        cmbBrand.addItem("SIN MARCA");

        cmbType.addItem("--NO SELECT--");
        cmbType.addItem("SIN CATEGORIA");

        cmbCounter.addItem("--NO SELECT--");
        cmbCounter.addItem("GENERAL");

        cmbInventory.addItem("--NO SELECT--");
        cmbPrice.addItem("--NO SELECT--");

        for (int i = 0; i < columnas_excel.length; i++) {
            cmbBCode.addItem(columnas_excel[i]);
        }

        for (int i = 0; i < columnas_excel.length; i++) {
            cmbDesc.addItem(columnas_excel[i]);
        }
        for (int i = 0; i < columnas_excel.length; i++) {
            cmbBrand.addItem(columnas_excel[i]);
        }
        for (int i = 0; i < columnas_excel.length; i++) {
            cmbType.addItem(columnas_excel[i]);
        }
        for (int i = 0; i < columnas_excel.length; i++) {
            cmbCounter.addItem(columnas_excel[i]);
        }
        for (int i = 0; i < columnas_excel.length; i++) {
            cmbInventory.addItem(columnas_excel[i]);
        }
        for (int i = 0; i < columnas_excel.length; i++) {
            cmbPrice.addItem(columnas_excel[i]);
        }
        Date fecha = new Date();
        DateFormat FormatoFecha = new SimpleDateFormat("dd-MM-YYYY");
        lblCreated.setText(FormatoFecha.format(fecha));
    }

