    public boolean checkFields() {
        boolean resp = false;
        if (cmbBCode.getSelectedIndex() == 0) {
            cmbBCode.requestFocus();
            resp = true;
        } else if (cmbDesc.getSelectedIndex() == 0) {
            cmbDesc.requestFocus();
            resp = true;
        } else if (cmbBrand.getSelectedIndex() == 0) {
            cmbBrand.requestFocus();
            resp = true;
        } else if (cmbType.getSelectedIndex() == 0) {
            cmbType.requestFocus();
            resp = true;
        } else if (cmbCounter.getSelectedIndex() == 0) {
            cmbCounter.requestFocus();
            resp = true;
        } else if (cmbInventory.getSelectedIndex() == 0) {
            cmbInventory.requestFocus();
            resp = true;
        } else if (cmbPrice.getSelectedIndex() == 0) {
            cmbPrice.requestFocus();
            resp = true;
        }
        return resp;
    }

    private void btnNextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNextActionPerformed
        setResponce();
    }//GEN-LAST:event_btnNextActionPerformed

    public void setResponce() {
        if (!checkFields()) {
            if (new Messages().confirmMessage("Importar Inventario", "¿Seguro desea importar Inventario?")) {
                this.headers = new int[7];
                headers[0] = cmbBCode.getSelectedIndex();
                headers[1] = cmbDesc.getSelectedIndex();
                headers[2] = cmbBrand.getSelectedIndex();
                headers[3] = cmbType.getSelectedIndex();
                headers[4] = cmbCounter.getSelectedIndex();
                headers[5] = cmbInventory.getSelectedIndex();
                headers[6] = cmbPrice.getSelectedIndex();
                this.dispose();
            }
        }
    }

