    public void updateAccount() {
        try {
            if (!txt_username.getText().equals(session.getLogedUser().getUsername())) {
                newUser mu = new newUser(null, true);
                mu.editUser(viewed);
                mu.show();
                if (mu.isNext()) {
                    reloadData();
                }

            } else {
                new Messages().aceptMessage("Actualizar Cuenta de Usuario", "Cuenta de Usuario en Uso...<br> no se puede editar");
            }
        } catch (Exception e) {
            new Messages().errorMessage(Class, "btnEditActionPerformed()", e);
        }
    }

