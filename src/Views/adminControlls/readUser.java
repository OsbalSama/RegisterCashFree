    public void dropAccount() {
        String text = "<html>"
                + "<center>"
                + "<b>"
                + "¡ESTA ACCIÓN ES IRREVERSIBLE!"
                + "</b>"
                + "<br>"
                + "<br>"
                + "¿Desea continuar?"
                + "</center>"
                + "</html>";
        if (new Messages().yesNoMessage("Eliminar Cuenta de Usuario", text)) {
            if (allUsers.drop(viewed)) {
                dispose();
            }
        }
    }

