    public void setContent(User viewed) {
        this.viewed = viewed;
        txt_id.setText(viewed.getId() + "");
        txt_username.setText(viewed.getUsername());
        txt_accslvl.setText(viewed.getRole());
        lblCreatedAt.setText("Creado: " + viewed.getCreatedToString());
        lblEditedAt.setText("- Editado: " + viewed.getEditedToString());
        if (session.getLogedUser().getUsername().equals(viewed.getUsername())) {
            lockControls();
        }
    }

    public void reloadData() {
        viewed = userController.getInstance().find(viewed.getId());
        txt_username.setText(viewed.getUsername());
        txt_accslvl.setText(viewed.getRole());
        lblCreatedAt.setText("Creado: " + viewed.getCreatedToString());
        lblEditedAt.setText("- Editado: " + viewed.getEditedToString());
    }

    public void lockControls() {
        btnEdit.setVisible(false);
        btnDelete.setVisible(false);
    }

