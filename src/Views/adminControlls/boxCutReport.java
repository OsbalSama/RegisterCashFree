    public void printDocument() {
        if (new Messages().yesNoMessage("Imprimir Venta", "Desea Imprimir Documento?")) {
            printerService selectPrinter = new printerService(sessionController.getInstance().getMainFrame(), true);
            selectPrinter.setData();
            selectPrinter.show();
            if (selectPrinter.isNext()) {
                String SelectedPrinter = selectPrinter.getSelectedPrinter();
                if (new ticketController().existBoxCur(boxcut)) {
                    System.out.println("existBoxCur");
                    if (new ticketController().rePrintBoxCut(boxcut, SelectedPrinter)) {
                        new Messages().aceptMessage("Imprimir Documento", "Documento Impreso Correctamente");
                    }
                } else {
                    System.out.println("createBoxCutPDF");
                    if (new ticketController().createBoxCutPDF(boxcut, SelectedPrinter)) {
                        new Messages().aceptMessage("Imprimir Documento", "Documento Impreso Correctamente");
                    }
                }
            }
        }
    }

