    public void setData(BoxCut boxcut) {
        this.boxcut = boxcut;
        lblIdReg.setText("Registro No." + this.boxcut.getId());
        lblSeller.setText("Creado por: " + this.boxcut.getUser());
        lblSaleDate.setText("Fecha de Registro: " + this.boxcut.getDateCreatedToString());
        lblSaleTime.setText("Hora de Registro: " + this.boxcut.getTimeCreatedToString());
        lblInCash.setText("En Caja: " + dataController.getInstance().getMoneyFormat(boxcut.getInCash()));
        lblToS.setText("Total en Ventas" + dataController.getInstance().getMoneyFormat(boxcut.getTotalOfSale()));
        btnClose.requestFocus();

        loadTable();
        showDatabase();
    }

    public void loadTable() {
        try {
            modelo = new DefaultTableModel() {
                @Override
                public boolean isCellEditable(int filas, int columnas) {
                    if (columnas < 0) {
                        return true;
                    } else {
                        return false;
                    }
                }
            };
            modelo.addColumn("Codigo");
            modelo.addColumn("Vendedor");
            modelo.addColumn("Total Venta");
            modelo.addColumn("Fecha Venta");
            modelo.addColumn("Hora Venta");
            tblData.setModel(modelo);
            tblData.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        } catch (Exception e) {
            new Messages().errorMessage(Class, "loadTable()", e);
        }
    }

    public void showDatabase() {
        modelo.setRowCount(0);
        try {
            List<Sale> Sales = DataBase.findSalesbyBoxCutId(boxcut.getId());
            if (!Sales.isEmpty()) {
                for (int i = 0; i < Sales.size(); i++) {
                    Sale temp = Sales.get(i);
                    String[] newRow = new String[6];
                    newRow[0] = temp.getId();
                    newRow[1] = temp.getSeller();
                    newRow[2] = dataController.getInstance().getMoneyFormat(temp.getTotalOfSale());
                    newRow[3] = temp.getDateCreatedToString();
                    newRow[4] = temp.getTimeCreatedToString();
                    modelo.addRow(newRow);
                }
            }
        } catch (Exception e) {
            new Messages().errorMessage(Class, "showDatabase()", e);
        }
    }

